int	ft_str_is_lowercase(char *str)
{
	int	i;
	int	j;

	j = 1;
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 97 && str[i] <= 122 && j == 1)
			j = 1;
		else
			j = 0;
		i++;
	}
	return (j);
}
