int	ft_find_next_prime(int nb)
{
	int	i;
	int	temp;

	i = 2;
	if (nb <= 1)
		return (2);
	while (i < 2147483647)
	{
		temp = 0;
		while (i <= nb / 2 && temp == 0)
		{
			if (nb % i == 0)
				temp = 1;
			if (nb / 2 == i)
				return (nb);
		i++;
		}
		nb++;
	}
	return (0);
}
